package Day2;

public class practice2 {
	
	public static void main(String[]args) {
		
		int a = 15;
		System.out.println(a);
		
		a = 0b1111;
		System.out.println(a);
		
		
		long b = 30000L;
		System.out.println(b);
		
		
		double c = 0.5;
		System.out.println(c);
		
		
		c= 5E-1;
		System.out.println(c);
		
		c= 3.14;
		System.out.println(c);
		
		float pi = 3.14f;
		System.out.println(pi);
		
	}

}
